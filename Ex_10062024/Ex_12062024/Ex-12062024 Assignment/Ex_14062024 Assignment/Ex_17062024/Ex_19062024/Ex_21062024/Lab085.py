#  dict is dictionary means unordered collection of data in a key-value pair format
# name is key, value is Ashwini
# name -> "Ashwini"
my_dict1 = {"name": "Ashwini", "age": "30", "address": "Bangalore"}
print(len(my_dict1))
print(my_dict1["name"])
print(my_dict1["age"])
(my_dict1["name"]) = "Sonakshi"
print(my_dict1["name"])
(my_dict1["age"]) = "4"
print(my_dict1["age"])
print(my_dict1["address"])
