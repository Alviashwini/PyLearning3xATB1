# Match case
numbers = int(input("Enter a Number\n"))
match numbers:
    case 1:
        print("you have entered 1")
    case 2:
        print("you have entered 2")
    case 3:
        print("you have entered 3")
    case 4:
        print("you have entered 4")
    case 5:
        print("you have entered 5")
    case _:
        print("No idea")



